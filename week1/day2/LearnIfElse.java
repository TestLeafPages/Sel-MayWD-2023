package week1.day2;

public class LearnIfElse {

	public static void main(String[] args) {
		int num=-2;
		if(num>0) {
			System.out.println("Given number positive: "+num);
		}else if(num==0) {
			System.out.println("neutral: "+num);
		}
		else {
			System.out.println("Given number negative: "+num);
		}
	}

}
